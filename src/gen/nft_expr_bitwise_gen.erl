-export([encode/1, decode/1, name/0]).

-define(NFTA_BITWISE_SREG, 1).
-define(NFTA_BITWISE_DREG, 2).
-define(NFTA_BITWISE_LEN, 3).
-define(NFTA_BITWISE_MASK, 4).
-define(NFTA_BITWISE_XOR, 5).
-define(NFTA_BITWISE_OP, 6).
-define(NFTA_BITWISE_DATA, 7).
-define(NFTA_BITWISE_SREG2, 8).

-doc "Encode a bitwise expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"bitwise">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> <<_:56>>.
name() -> <<"bitwise">>.

%% --- Attribute encoders ---

encode_attr(sreg, V) -> nfnl_attr:encode_u32(?NFTA_BITWISE_SREG, V);
encode_attr(dreg, V) -> nfnl_attr:encode_u32(?NFTA_BITWISE_DREG, V);
encode_attr(len, V) -> nfnl_attr:encode_u32(?NFTA_BITWISE_LEN, V);
encode_attr(mask, V) -> nfnl_attr:encode_nested(?NFTA_BITWISE_MASK, V);
encode_attr('xor', V) -> nfnl_attr:encode_nested(?NFTA_BITWISE_XOR, V);
encode_attr(op, V) -> nfnl_attr:encode_u32(?NFTA_BITWISE_OP, V);
encode_attr(data, V) -> nfnl_attr:encode_u32(?NFTA_BITWISE_DATA, V);
encode_attr(sreg2, V) -> nfnl_attr:encode_u32(?NFTA_BITWISE_SREG2, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_bitwise_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode bitwise attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_BITWISE_SREG, <<V:32/big>>}, Acc) -> Acc#{sreg => V};
decode_attr({?NFTA_BITWISE_DREG, <<V:32/big>>}, Acc) -> Acc#{dreg => V};
decode_attr({?NFTA_BITWISE_LEN, <<V:32/big>>}, Acc) -> Acc#{len => V};
decode_attr({?NFTA_BITWISE_MASK, nested, V}, Acc) -> Acc#{mask => V};
decode_attr({?NFTA_BITWISE_XOR, nested, V}, Acc) -> Acc#{'xor' => V};
decode_attr({?NFTA_BITWISE_OP, <<V:32/big>>}, Acc) -> Acc#{op => V};
decode_attr({?NFTA_BITWISE_DATA, V}, Acc) -> Acc#{data => V};
decode_attr({?NFTA_BITWISE_SREG2, <<V:32/big>>}, Acc) -> Acc#{sreg2 => V};
decode_attr(_, Acc) -> Acc.
