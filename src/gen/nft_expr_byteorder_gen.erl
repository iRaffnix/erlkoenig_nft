-module(nft_expr_byteorder_gen).
-moduledoc """
nf_tables byteorder expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_BYTEORDER_SREG = 1  (u32)  source register
%%   NFTA_BYTEORDER_DREG = 2  (u32)  destination register
%%   NFTA_BYTEORDER_OP = 3  (u32)  operator
%%   NFTA_BYTEORDER_LEN = 4  (u32)  length of the data
%%   NFTA_BYTEORDER_SIZE = 5  (u32)  data size in bytes

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_byteorder.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_BYTEORDER_SREG, 1).
-define(NFTA_BYTEORDER_DREG, 2).
-define(NFTA_BYTEORDER_OP, 3).
-define(NFTA_BYTEORDER_LEN, 4).
-define(NFTA_BYTEORDER_SIZE, 5).

-doc "Encode a byteorder expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"byteorder">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> <<_:72>>.
name() -> <<"byteorder">>.

%% --- Attribute encoders ---

encode_attr(sreg, V) -> nfnl_attr:encode_u32(?NFTA_BYTEORDER_SREG, V);
encode_attr(dreg, V) -> nfnl_attr:encode_u32(?NFTA_BYTEORDER_DREG, V);
encode_attr(op, V) -> nfnl_attr:encode_u32(?NFTA_BYTEORDER_OP, V);
encode_attr(len, V) -> nfnl_attr:encode_u32(?NFTA_BYTEORDER_LEN, V);
encode_attr(size, V) -> nfnl_attr:encode_u32(?NFTA_BYTEORDER_SIZE, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_byteorder_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode byteorder attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_BYTEORDER_SREG, <<V:32/big>>}, Acc) -> Acc#{sreg => V};
decode_attr({?NFTA_BYTEORDER_DREG, <<V:32/big>>}, Acc) -> Acc#{dreg => V};
decode_attr({?NFTA_BYTEORDER_OP, <<V:32/big>>}, Acc) -> Acc#{op => V};
decode_attr({?NFTA_BYTEORDER_LEN, <<V:32/big>>}, Acc) -> Acc#{len => V};
decode_attr({?NFTA_BYTEORDER_SIZE, <<V:32/big>>}, Acc) -> Acc#{size => V};
decode_attr(_, Acc) -> Acc.
