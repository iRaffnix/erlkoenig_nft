-module(nft_expr_cmp_gen).
-moduledoc """
nf_tables cmp expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_CMP_SREG = 1  (u32)  source register of data to compare
%%   NFTA_CMP_OP = 2  (u32)  cmp operation
%%   NFTA_CMP_DATA = 3  (nested)  data to compare against

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_cmp.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_CMP_SREG, 1).
-define(NFTA_CMP_OP, 2).
-define(NFTA_CMP_DATA, 3).

-doc "Encode a cmp expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"cmp">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> <<_:24>>.
name() -> <<"cmp">>.

%% --- Attribute encoders ---

encode_attr(sreg, V) -> nfnl_attr:encode_u32(?NFTA_CMP_SREG, V);
encode_attr(op, V) -> nfnl_attr:encode_u32(?NFTA_CMP_OP, V);
encode_attr(data, V) -> nfnl_attr:encode_nested(?NFTA_CMP_DATA, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_cmp_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode cmp attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_CMP_SREG, <<V:32/big>>}, Acc) -> Acc#{sreg => V};
decode_attr({?NFTA_CMP_OP, <<V:32/big>>}, Acc) -> Acc#{op => V};
decode_attr({?NFTA_CMP_DATA, nested, V}, Acc) -> Acc#{data => V};
decode_attr(_, Acc) -> Acc.
