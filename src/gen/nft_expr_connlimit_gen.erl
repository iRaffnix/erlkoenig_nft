-module(nft_expr_connlimit_gen).
-moduledoc """
nf_tables connlimit expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_CONNLIMIT_COUNT = 1  (u32)  number of connections
%%   NFTA_CONNLIMIT_FLAGS = 2  (u32)  flags

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_connlimit.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_CONNLIMIT_COUNT, 1).
-define(NFTA_CONNLIMIT_FLAGS, 2).

-doc "Encode a connlimit expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"connlimit">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> <<_:72>>.
name() -> <<"connlimit">>.

%% --- Attribute encoders ---

encode_attr(count, V) -> nfnl_attr:encode_u32(?NFTA_CONNLIMIT_COUNT, V);
encode_attr(flags, V) -> nfnl_attr:encode_u32(?NFTA_CONNLIMIT_FLAGS, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_connlimit_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode connlimit attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_CONNLIMIT_COUNT, <<V:32/big>>}, Acc) -> Acc#{count => V};
decode_attr({?NFTA_CONNLIMIT_FLAGS, <<V:32/big>>}, Acc) -> Acc#{flags => V};
decode_attr(_, Acc) -> Acc.
