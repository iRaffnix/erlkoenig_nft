-module(nft_expr_counter_gen).
-moduledoc """
nf_tables counter expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_COUNTER_BYTES = 1  (u64)  number of bytes
%%   NFTA_COUNTER_PACKETS = 2  (u64)  number of packets

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_counter.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_COUNTER_BYTES, 1).
-define(NFTA_COUNTER_PACKETS, 2).

-doc "Encode a counter expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"counter">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> binary().
name() -> <<"counter">>.

%% --- Attribute encoders ---

encode_attr(bytes, V) -> nfnl_attr:encode_u64(?NFTA_COUNTER_BYTES, V);
encode_attr(packets, V) -> nfnl_attr:encode_u64(?NFTA_COUNTER_PACKETS, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_counter_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode counter attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_COUNTER_BYTES, <<V:64/big>>}, Acc) -> Acc#{bytes => V};
decode_attr({?NFTA_COUNTER_PACKETS, <<V:64/big>>}, Acc) -> Acc#{packets => V};
decode_attr(_, Acc) -> Acc.
