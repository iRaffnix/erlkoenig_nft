-module(nft_expr_ct_gen).
-moduledoc """
nf_tables ct expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_CT_DREG = 1  (u32)  destination register
%%   NFTA_CT_KEY = 2  (u32)  conntrack data item to load
%%   NFTA_CT_DIRECTION = 3  (u8)  direction in case of directional keys
%%   NFTA_CT_SREG = 4  (u32)  source register

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_ct.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_CT_DREG, 1).
-define(NFTA_CT_KEY, 2).
-define(NFTA_CT_DIRECTION, 3).
-define(NFTA_CT_SREG, 4).

-doc "Encode a ct expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"ct">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> binary().
name() -> <<"ct">>.

%% --- Attribute encoders ---

encode_attr(dreg, V) -> nfnl_attr:encode_u32(?NFTA_CT_DREG, V);
encode_attr(key, V) -> nfnl_attr:encode_u32(?NFTA_CT_KEY, V);
encode_attr(direction, V) -> nfnl_attr:encode(?NFTA_CT_DIRECTION, <<V:8>>);
encode_attr(sreg, V) -> nfnl_attr:encode_u32(?NFTA_CT_SREG, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_ct_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode ct attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_CT_DREG, <<V:32/big>>}, Acc) -> Acc#{dreg => V};
decode_attr({?NFTA_CT_KEY, <<V:32/big>>}, Acc) -> Acc#{key => V};
decode_attr({?NFTA_CT_DIRECTION, <<V:8>>}, Acc) -> Acc#{direction => V};
decode_attr({?NFTA_CT_SREG, <<V:32/big>>}, Acc) -> Acc#{sreg => V};
decode_attr(_, Acc) -> Acc.
