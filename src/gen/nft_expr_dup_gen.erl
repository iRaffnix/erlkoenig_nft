-module(nft_expr_dup_gen).
-moduledoc """
nf_tables dup expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_DUP_SREG_ADDR = 1  (u32)  source register of address
%%   NFTA_DUP_SREG_DEV = 2  (u32)  source register of output interface

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_dup.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_DUP_SREG_ADDR, 1).
-define(NFTA_DUP_SREG_DEV, 2).

-doc "Encode a dup expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"dup">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> <<_:24>>.
name() -> <<"dup">>.

%% --- Attribute encoders ---

encode_attr(sreg_addr, V) -> nfnl_attr:encode_u32(?NFTA_DUP_SREG_ADDR, V);
encode_attr(sreg_dev, V) -> nfnl_attr:encode_u32(?NFTA_DUP_SREG_DEV, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_dup_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode dup attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_DUP_SREG_ADDR, <<V:32/big>>}, Acc) -> Acc#{sreg_addr => V};
decode_attr({?NFTA_DUP_SREG_DEV, <<V:32/big>>}, Acc) -> Acc#{sreg_dev => V};
decode_attr(_, Acc) -> Acc.
