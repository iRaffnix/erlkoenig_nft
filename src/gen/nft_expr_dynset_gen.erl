-module(nft_expr_dynset_gen).
-moduledoc """
nf_tables dynset expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_DYNSET_SET_NAME = 1  (string)  name of set the to add data to
%%   NFTA_DYNSET_SET_ID = 2  (u32)  uniquely identifier of the set in the transaction
%%   NFTA_DYNSET_OP = 3  (u32)  operation
%%   NFTA_DYNSET_SREG_KEY = 4  (u32)  source register of the key
%%   NFTA_DYNSET_SREG_DATA = 5  (u32)  source register of the data
%%   NFTA_DYNSET_TIMEOUT = 6  (u64)  timeout value for the new element
%%   NFTA_DYNSET_EXPR = 7  (nested)  expression
%%   NFTA_DYNSET_FLAGS = 9  (u32)  flags
%%   NFTA_DYNSET_EXPRESSIONS = 10  (nested)  list of expressions

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_dynset.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_DYNSET_SET_NAME, 1).
-define(NFTA_DYNSET_SET_ID, 2).
-define(NFTA_DYNSET_OP, 3).
-define(NFTA_DYNSET_SREG_KEY, 4).
-define(NFTA_DYNSET_SREG_DATA, 5).
-define(NFTA_DYNSET_TIMEOUT, 6).
-define(NFTA_DYNSET_EXPR, 7).
-define(NFTA_DYNSET_FLAGS, 9).
-define(NFTA_DYNSET_EXPRESSIONS, 10).

-doc "Encode a dynset expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"dynset">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> <<_:48>>.
name() -> <<"dynset">>.

%% --- Attribute encoders ---

encode_attr(set_name, V) -> nfnl_attr:encode_str(?NFTA_DYNSET_SET_NAME, V);
encode_attr(set_id, V) -> nfnl_attr:encode_u32(?NFTA_DYNSET_SET_ID, V);
encode_attr(op, V) -> nfnl_attr:encode_u32(?NFTA_DYNSET_OP, V);
encode_attr(sreg_key, V) -> nfnl_attr:encode_u32(?NFTA_DYNSET_SREG_KEY, V);
encode_attr(sreg_data, V) -> nfnl_attr:encode_u32(?NFTA_DYNSET_SREG_DATA, V);
encode_attr(timeout, V) -> nfnl_attr:encode_u64(?NFTA_DYNSET_TIMEOUT, V);
encode_attr(expr, V) -> nfnl_attr:encode_nested(?NFTA_DYNSET_EXPR, V);
encode_attr(flags, V) -> nfnl_attr:encode_u32(?NFTA_DYNSET_FLAGS, V);
encode_attr(expressions, V) -> nfnl_attr:encode_nested(?NFTA_DYNSET_EXPRESSIONS, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_dynset_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode dynset attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_DYNSET_SET_NAME, V}, Acc) when is_binary(V) -> Acc#{set_name => strip_null(V)};
decode_attr({?NFTA_DYNSET_SET_ID, <<V:32/big>>}, Acc) -> Acc#{set_id => V};
decode_attr({?NFTA_DYNSET_OP, <<V:32/big>>}, Acc) -> Acc#{op => V};
decode_attr({?NFTA_DYNSET_SREG_KEY, <<V:32/big>>}, Acc) -> Acc#{sreg_key => V};
decode_attr({?NFTA_DYNSET_SREG_DATA, <<V:32/big>>}, Acc) -> Acc#{sreg_data => V};
decode_attr({?NFTA_DYNSET_TIMEOUT, <<V:64/big>>}, Acc) -> Acc#{timeout => V};
decode_attr({?NFTA_DYNSET_EXPR, nested, V}, Acc) -> Acc#{expr => V};
decode_attr({?NFTA_DYNSET_FLAGS, <<V:32/big>>}, Acc) -> Acc#{flags => V};
decode_attr({?NFTA_DYNSET_EXPRESSIONS, nested, V}, Acc) -> Acc#{expressions => V};
decode_attr(_, Acc) -> Acc.

%% --- Internal ---

strip_null(Bin) when is_binary(Bin) ->
    case binary:match(Bin, <<0>>) of
        {Pos, _} -> binary:part(Bin, 0, Pos);
        nomatch -> Bin
    end.
