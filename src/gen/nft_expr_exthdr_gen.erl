-module(nft_expr_exthdr_gen).
-moduledoc """
nf_tables exthdr expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_EXTHDR_DREG = 1  (u32)  destination register
%%   NFTA_EXTHDR_TYPE = 2  (u8)  extension header type
%%   NFTA_EXTHDR_OFFSET = 3  (u32)  extension header offset
%%   NFTA_EXTHDR_LEN = 4  (u32)  extension header length
%%   NFTA_EXTHDR_FLAGS = 5  (u32)  extension header flags
%%   NFTA_EXTHDR_OP = 6  (u32)  option match type
%%   NFTA_EXTHDR_SREG = 7  (u32)  option match type

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_exthdr.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_EXTHDR_DREG, 1).
-define(NFTA_EXTHDR_TYPE, 2).
-define(NFTA_EXTHDR_OFFSET, 3).
-define(NFTA_EXTHDR_LEN, 4).
-define(NFTA_EXTHDR_FLAGS, 5).
-define(NFTA_EXTHDR_OP, 6).
-define(NFTA_EXTHDR_SREG, 7).

-doc "Encode a exthdr expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"exthdr">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> <<_:48>>.
name() -> <<"exthdr">>.

%% --- Attribute encoders ---

encode_attr(dreg, V) -> nfnl_attr:encode_u32(?NFTA_EXTHDR_DREG, V);
encode_attr(type, V) -> nfnl_attr:encode(?NFTA_EXTHDR_TYPE, <<V:8>>);
encode_attr(offset, V) -> nfnl_attr:encode_u32(?NFTA_EXTHDR_OFFSET, V);
encode_attr(len, V) -> nfnl_attr:encode_u32(?NFTA_EXTHDR_LEN, V);
encode_attr(flags, V) -> nfnl_attr:encode_u32(?NFTA_EXTHDR_FLAGS, V);
encode_attr(op, V) -> nfnl_attr:encode_u32(?NFTA_EXTHDR_OP, V);
encode_attr(sreg, V) -> nfnl_attr:encode_u32(?NFTA_EXTHDR_SREG, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_exthdr_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode exthdr attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_EXTHDR_DREG, <<V:32/big>>}, Acc) -> Acc#{dreg => V};
decode_attr({?NFTA_EXTHDR_TYPE, <<V:8>>}, Acc) -> Acc#{type => V};
decode_attr({?NFTA_EXTHDR_OFFSET, <<V:32/big>>}, Acc) -> Acc#{offset => V};
decode_attr({?NFTA_EXTHDR_LEN, <<V:32/big>>}, Acc) -> Acc#{len => V};
decode_attr({?NFTA_EXTHDR_FLAGS, <<V:32/big>>}, Acc) -> Acc#{flags => V};
decode_attr({?NFTA_EXTHDR_OP, <<V:32/big>>}, Acc) -> Acc#{op => V};
decode_attr({?NFTA_EXTHDR_SREG, <<V:32/big>>}, Acc) -> Acc#{sreg => V};
decode_attr(_, Acc) -> Acc.
