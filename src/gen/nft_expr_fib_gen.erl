-module(nft_expr_fib_gen).
-moduledoc """
nf_tables fib expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_FIB_DREG = 1  (unknown)  
%%   NFTA_FIB_RESULT = 2  (unknown)  
%%   NFTA_FIB_FLAGS = 3  (unknown)  

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_fib.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_FIB_DREG, 1).
-define(NFTA_FIB_RESULT, 2).
-define(NFTA_FIB_FLAGS, 3).

-doc "Encode a fib expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"fib">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> binary().
name() -> <<"fib">>.

%% --- Attribute encoders ---

encode_attr(dreg, V) -> nfnl_attr:encode_u32(?NFTA_FIB_DREG, V);
encode_attr(result, V) -> nfnl_attr:encode_u32(?NFTA_FIB_RESULT, V);
encode_attr(flags, V) -> nfnl_attr:encode_u32(?NFTA_FIB_FLAGS, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_fib_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode fib attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_FIB_DREG, <<V:32/big>>}, Acc) -> Acc#{dreg => V};
decode_attr({?NFTA_FIB_RESULT, <<V:32/big>>}, Acc) -> Acc#{result => V};
decode_attr({?NFTA_FIB_FLAGS, <<V:32/big>>}, Acc) -> Acc#{flags => V};
decode_attr(_, Acc) -> Acc.
