-module(nft_expr_fwd_gen).
-moduledoc """
nf_tables fwd expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_FWD_SREG_DEV = 1  (u32)  source register of output interface
%%   NFTA_FWD_SREG_ADDR = 2  (u32)  source register of destination address
%%   NFTA_FWD_NFPROTO = 3  (u32)  layer 3 family of source register address

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_fwd.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_FWD_SREG_DEV, 1).
-define(NFTA_FWD_SREG_ADDR, 2).
-define(NFTA_FWD_NFPROTO, 3).

-doc "Encode a fwd expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"fwd">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> <<_:24>>.
name() -> <<"fwd">>.

%% --- Attribute encoders ---

encode_attr(sreg_dev, V) -> nfnl_attr:encode_u32(?NFTA_FWD_SREG_DEV, V);
encode_attr(sreg_addr, V) -> nfnl_attr:encode_u32(?NFTA_FWD_SREG_ADDR, V);
encode_attr(nfproto, V) -> nfnl_attr:encode_u32(?NFTA_FWD_NFPROTO, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_fwd_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode fwd attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_FWD_SREG_DEV, <<V:32/big>>}, Acc) -> Acc#{sreg_dev => V};
decode_attr({?NFTA_FWD_SREG_ADDR, <<V:32/big>>}, Acc) -> Acc#{sreg_addr => V};
decode_attr({?NFTA_FWD_NFPROTO, <<V:32/big>>}, Acc) -> Acc#{nfproto => V};
decode_attr(_, Acc) -> Acc.
