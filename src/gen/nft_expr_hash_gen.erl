-module(nft_expr_hash_gen).
-moduledoc """
nf_tables hash expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_HASH_SREG = 1  (u32)  source register
%%   NFTA_HASH_DREG = 2  (u32)  destination register
%%   NFTA_HASH_LEN = 3  (u32)  source data length
%%   NFTA_HASH_MODULUS = 4  (u32)  modulus value
%%   NFTA_HASH_SEED = 5  (u32)  seed value
%%   NFTA_HASH_OFFSET = 6  (u32)  add this offset value to hash result
%%   NFTA_HASH_TYPE = 7  (u32)  hash operation

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_hash.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_HASH_SREG, 1).
-define(NFTA_HASH_DREG, 2).
-define(NFTA_HASH_LEN, 3).
-define(NFTA_HASH_MODULUS, 4).
-define(NFTA_HASH_SEED, 5).
-define(NFTA_HASH_OFFSET, 6).
-define(NFTA_HASH_TYPE, 7).

-doc "Encode a hash expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"hash">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> binary().
name() -> <<"hash">>.

%% --- Attribute encoders ---

encode_attr(sreg, V) -> nfnl_attr:encode_u32(?NFTA_HASH_SREG, V);
encode_attr(dreg, V) -> nfnl_attr:encode_u32(?NFTA_HASH_DREG, V);
encode_attr(len, V) -> nfnl_attr:encode_u32(?NFTA_HASH_LEN, V);
encode_attr(modulus, V) -> nfnl_attr:encode_u32(?NFTA_HASH_MODULUS, V);
encode_attr(seed, V) -> nfnl_attr:encode_u32(?NFTA_HASH_SEED, V);
encode_attr(offset, V) -> nfnl_attr:encode_u32(?NFTA_HASH_OFFSET, V);
encode_attr(type, V) -> nfnl_attr:encode_u32(?NFTA_HASH_TYPE, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_hash_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode hash attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_HASH_SREG, <<V:32/big>>}, Acc) -> Acc#{sreg => V};
decode_attr({?NFTA_HASH_DREG, <<V:32/big>>}, Acc) -> Acc#{dreg => V};
decode_attr({?NFTA_HASH_LEN, <<V:32/big>>}, Acc) -> Acc#{len => V};
decode_attr({?NFTA_HASH_MODULUS, <<V:32/big>>}, Acc) -> Acc#{modulus => V};
decode_attr({?NFTA_HASH_SEED, <<V:32/big>>}, Acc) -> Acc#{seed => V};
decode_attr({?NFTA_HASH_OFFSET, <<V:32/big>>}, Acc) -> Acc#{offset => V};
decode_attr({?NFTA_HASH_TYPE, <<V:32/big>>}, Acc) -> Acc#{type => V};
decode_attr(_, Acc) -> Acc.
