-module(nft_expr_immediate_gen).
-moduledoc """
nf_tables immediate expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_IMMEDIATE_DREG = 1  (u32)  destination register to load data into
%%   NFTA_IMMEDIATE_DATA = 2  (nested)  data to load

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_immediate.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_IMMEDIATE_DREG, 1).
-define(NFTA_IMMEDIATE_DATA, 2).

-doc "Encode a immediate expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"immediate">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> binary().
name() -> <<"immediate">>.

%% --- Attribute encoders ---

encode_attr(dreg, V) -> nfnl_attr:encode_u32(?NFTA_IMMEDIATE_DREG, V);
encode_attr(data, V) -> nfnl_attr:encode_nested(?NFTA_IMMEDIATE_DATA, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_immediate_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode immediate attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_IMMEDIATE_DREG, <<V:32/big>>}, Acc) -> Acc#{dreg => V};
decode_attr({?NFTA_IMMEDIATE_DATA, nested, V}, Acc) -> Acc#{data => V};
decode_attr(_, Acc) -> Acc.
