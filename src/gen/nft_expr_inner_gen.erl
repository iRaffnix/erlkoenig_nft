encode_attr(type, V) -> nfnl_attr:encode_u32(?NFTA_INNER_TYPE, V);
encode_attr(flags, V) -> nfnl_attr:encode_u32(?NFTA_INNER_FLAGS, V);
encode_attr(hdrsize, V) -> nfnl_attr:encode_u32(?NFTA_INNER_HDRSIZE, V);
encode_attr(expr, V) -> nfnl_attr:encode_u32(?NFTA_INNER_EXPR, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_inner_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode inner attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_INNER_NUM, V}, Acc) -> Acc#{num => V};
