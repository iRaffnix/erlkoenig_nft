-module(nft_expr_last_gen).
-moduledoc """
nf_tables last expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_LAST_SET = 1  (u32)  last update has been set, zero means never updated
%%   NFTA_LAST_MSECS = 2  (u64)  milliseconds since last update

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_last.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_LAST_SET, 1).
-define(NFTA_LAST_MSECS, 2).

-doc "Encode a last expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"last">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> <<_:32>>.
name() -> <<"last">>.

%% --- Attribute encoders ---

encode_attr(set, V) -> nfnl_attr:encode_u32(?NFTA_LAST_SET, V);
encode_attr(msecs, V) -> nfnl_attr:encode_u64(?NFTA_LAST_MSECS, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_last_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode last attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_LAST_SET, <<V:32/big>>}, Acc) -> Acc#{set => V};
decode_attr({?NFTA_LAST_MSECS, <<V:64/big>>}, Acc) -> Acc#{msecs => V};
decode_attr(_, Acc) -> Acc.
