-module(nft_expr_limit_gen).
-moduledoc """
nf_tables limit expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_LIMIT_RATE = 1  (u64)  refill rate
%%   NFTA_LIMIT_UNIT = 2  (u64)  refill unit
%%   NFTA_LIMIT_BURST = 3  (u32)  burst
%%   NFTA_LIMIT_TYPE = 4  (u32)  type of limit
%%   NFTA_LIMIT_FLAGS = 5  (u32)  flags

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_limit.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_LIMIT_RATE, 1).
-define(NFTA_LIMIT_UNIT, 2).
-define(NFTA_LIMIT_BURST, 3).
-define(NFTA_LIMIT_TYPE, 4).
-define(NFTA_LIMIT_FLAGS, 5).

-doc "Encode a limit expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"limit">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> <<_:40>>.
name() -> <<"limit">>.

%% --- Attribute encoders ---

encode_attr(rate, V) -> nfnl_attr:encode_u64(?NFTA_LIMIT_RATE, V);
encode_attr(unit, V) -> nfnl_attr:encode_u64(?NFTA_LIMIT_UNIT, V);
encode_attr(burst, V) -> nfnl_attr:encode_u32(?NFTA_LIMIT_BURST, V);
encode_attr(type, V) -> nfnl_attr:encode_u32(?NFTA_LIMIT_TYPE, V);
encode_attr(flags, V) -> nfnl_attr:encode_u32(?NFTA_LIMIT_FLAGS, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_limit_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode limit attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_LIMIT_RATE, <<V:64/big>>}, Acc) -> Acc#{rate => V};
decode_attr({?NFTA_LIMIT_UNIT, <<V:64/big>>}, Acc) -> Acc#{unit => V};
decode_attr({?NFTA_LIMIT_BURST, <<V:32/big>>}, Acc) -> Acc#{burst => V};
decode_attr({?NFTA_LIMIT_TYPE, <<V:32/big>>}, Acc) -> Acc#{type => V};
decode_attr({?NFTA_LIMIT_FLAGS, <<V:32/big>>}, Acc) -> Acc#{flags => V};
decode_attr(_, Acc) -> Acc.
