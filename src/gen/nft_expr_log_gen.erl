-module(nft_expr_log_gen).
-moduledoc """
nf_tables log expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_LOG_GROUP = 1  (u32)  netlink group to send messages to
%%   NFTA_LOG_PREFIX = 2  (string)  prefix to prepend to log messages
%%   NFTA_LOG_SNAPLEN = 3  (u32)  length of payload to include in netlink message
%%   NFTA_LOG_QTHRESHOLD = 4  (u32)  queue threshold
%%   NFTA_LOG_LEVEL = 5  (u32)  log level
%%   NFTA_LOG_FLAGS = 6  (u32)  logging flags

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_log.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_LOG_GROUP, 1).
-define(NFTA_LOG_PREFIX, 2).
-define(NFTA_LOG_SNAPLEN, 3).
-define(NFTA_LOG_QTHRESHOLD, 4).
-define(NFTA_LOG_LEVEL, 5).
-define(NFTA_LOG_FLAGS, 6).

-doc "Encode a log expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"log">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> <<_:24>>.
name() -> <<"log">>.

%% --- Attribute encoders ---

encode_attr(group, V) -> nfnl_attr:encode_u32(?NFTA_LOG_GROUP, V);
encode_attr(prefix, V) -> nfnl_attr:encode_str(?NFTA_LOG_PREFIX, V);
encode_attr(snaplen, V) -> nfnl_attr:encode_u32(?NFTA_LOG_SNAPLEN, V);
encode_attr(qthreshold, V) -> nfnl_attr:encode_u32(?NFTA_LOG_QTHRESHOLD, V);
encode_attr(level, V) -> nfnl_attr:encode_u32(?NFTA_LOG_LEVEL, V);
encode_attr(flags, V) -> nfnl_attr:encode_u32(?NFTA_LOG_FLAGS, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_log_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode log attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_LOG_GROUP, <<V:32/big>>}, Acc) -> Acc#{group => V};
decode_attr({?NFTA_LOG_PREFIX, V}, Acc) when is_binary(V) -> Acc#{prefix => strip_null(V)};
decode_attr({?NFTA_LOG_SNAPLEN, <<V:32/big>>}, Acc) -> Acc#{snaplen => V};
decode_attr({?NFTA_LOG_QTHRESHOLD, <<V:32/big>>}, Acc) -> Acc#{qthreshold => V};
decode_attr({?NFTA_LOG_LEVEL, <<V:32/big>>}, Acc) -> Acc#{level => V};
decode_attr({?NFTA_LOG_FLAGS, <<V:32/big>>}, Acc) -> Acc#{flags => V};
decode_attr(_, Acc) -> Acc.

%% --- Internal ---

strip_null(Bin) when is_binary(Bin) ->
    case binary:match(Bin, <<0>>) of
        {Pos, _} -> binary:part(Bin, 0, Pos);
        nomatch -> Bin
    end.
