-module(nft_expr_lookup_gen).
-moduledoc """
nf_tables lookup expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_LOOKUP_SET = 1  (string)  name of the set where to look for
%%   NFTA_LOOKUP_SREG = 2  (u32)  source register of the data to look for
%%   NFTA_LOOKUP_DREG = 3  (u32)  destination register
%%   NFTA_LOOKUP_SET_ID = 4  (u32)  uniquely identifies a set in a transaction
%%   NFTA_LOOKUP_FLAGS = 5  (u32)  flags

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_lookup.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_LOOKUP_SET, 1).
-define(NFTA_LOOKUP_SREG, 2).
-define(NFTA_LOOKUP_DREG, 3).
-define(NFTA_LOOKUP_SET_ID, 4).
-define(NFTA_LOOKUP_FLAGS, 5).

-doc "Encode a lookup expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"lookup">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> <<_:48>>.
name() -> <<"lookup">>.

%% --- Attribute encoders ---

encode_attr(set, V) -> nfnl_attr:encode_str(?NFTA_LOOKUP_SET, V);
encode_attr(sreg, V) -> nfnl_attr:encode_u32(?NFTA_LOOKUP_SREG, V);
encode_attr(dreg, V) -> nfnl_attr:encode_u32(?NFTA_LOOKUP_DREG, V);
encode_attr(set_id, V) -> nfnl_attr:encode_u32(?NFTA_LOOKUP_SET_ID, V);
encode_attr(flags, V) -> nfnl_attr:encode_u32(?NFTA_LOOKUP_FLAGS, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_lookup_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode lookup attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_LOOKUP_SET, V}, Acc) when is_binary(V) -> Acc#{set => strip_null(V)};
decode_attr({?NFTA_LOOKUP_SREG, <<V:32/big>>}, Acc) -> Acc#{sreg => V};
decode_attr({?NFTA_LOOKUP_DREG, <<V:32/big>>}, Acc) -> Acc#{dreg => V};
decode_attr({?NFTA_LOOKUP_SET_ID, <<V:32/big>>}, Acc) -> Acc#{set_id => V};
decode_attr({?NFTA_LOOKUP_FLAGS, <<V:32/big>>}, Acc) -> Acc#{flags => V};
decode_attr(_, Acc) -> Acc.

%% --- Internal ---

strip_null(Bin) when is_binary(Bin) ->
    case binary:match(Bin, <<0>>) of
        {Pos, _} -> binary:part(Bin, 0, Pos);
        nomatch -> Bin
    end.
