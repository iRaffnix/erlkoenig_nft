-module(nft_expr_masq_gen).
-moduledoc """
nf_tables masq expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_MASQ_FLAGS = 1  (u32)  NAT flags (see NF_NAT_RANGE_* in linux/netfilter/nf_nat.h)
%%   NFTA_MASQ_REG_PROTO_MIN = 2  (u32)  source register of proto range start
%%   NFTA_MASQ_REG_PROTO_MAX = 3  (u32)  source register of proto range end

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_masq.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_MASQ_FLAGS, 1).
-define(NFTA_MASQ_REG_PROTO_MIN, 2).
-define(NFTA_MASQ_REG_PROTO_MAX, 3).

-doc "Encode a masq expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"masq">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> <<_:32>>.
name() -> <<"masq">>.

%% --- Attribute encoders ---

encode_attr(flags, V) -> nfnl_attr:encode_u32(?NFTA_MASQ_FLAGS, V);
encode_attr(reg_proto_min, V) -> nfnl_attr:encode_u32(?NFTA_MASQ_REG_PROTO_MIN, V);
encode_attr(reg_proto_max, V) -> nfnl_attr:encode_u32(?NFTA_MASQ_REG_PROTO_MAX, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_masq_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode masq attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_MASQ_FLAGS, <<V:32/big>>}, Acc) -> Acc#{flags => V};
decode_attr({?NFTA_MASQ_REG_PROTO_MIN, <<V:32/big>>}, Acc) -> Acc#{reg_proto_min => V};
decode_attr({?NFTA_MASQ_REG_PROTO_MAX, <<V:32/big>>}, Acc) -> Acc#{reg_proto_max => V};
decode_attr(_, Acc) -> Acc.
