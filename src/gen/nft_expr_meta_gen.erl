-module(nft_expr_meta_gen).
-moduledoc """
nf_tables meta expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_META_DREG = 1  (u32)  destination register
%%   NFTA_META_KEY = 2  (u32)  meta data item to load
%%   NFTA_META_SREG = 3  (u32)  source register

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_meta.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_META_DREG, 1).
-define(NFTA_META_KEY, 2).
-define(NFTA_META_SREG, 3).

-doc "Encode a meta expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"meta">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> <<_:32>>.
name() -> <<"meta">>.

%% --- Attribute encoders ---

encode_attr(dreg, V) -> nfnl_attr:encode_u32(?NFTA_META_DREG, V);
encode_attr(key, V) -> nfnl_attr:encode_u32(?NFTA_META_KEY, V);
encode_attr(sreg, V) -> nfnl_attr:encode_u32(?NFTA_META_SREG, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_meta_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode meta attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_META_DREG, <<V:32/big>>}, Acc) -> Acc#{dreg => V};
decode_attr({?NFTA_META_KEY, <<V:32/big>>}, Acc) -> Acc#{key => V};
decode_attr({?NFTA_META_SREG, <<V:32/big>>}, Acc) -> Acc#{sreg => V};
decode_attr(_, Acc) -> Acc.
