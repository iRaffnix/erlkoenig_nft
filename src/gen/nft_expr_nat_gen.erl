-module(nft_expr_nat_gen).
-moduledoc """
nf_tables nat expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_NAT_TYPE = 1  (u32)  NAT type
%%   NFTA_NAT_FAMILY = 2  (u32)  NAT family
%%   NFTA_NAT_REG_ADDR_MIN = 3  (u32)  source register of address range start
%%   NFTA_NAT_REG_ADDR_MAX = 4  (u32)  source register of address range end
%%   NFTA_NAT_REG_PROTO_MIN = 5  (u32)  source register of proto range start
%%   NFTA_NAT_REG_PROTO_MAX = 6  (u32)  source register of proto range end
%%   NFTA_NAT_FLAGS = 7  (u32)  NAT flags (see NF_NAT_RANGE_* in linux/netfilter/nf_nat.h)

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_nat.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_NAT_TYPE, 1).
-define(NFTA_NAT_FAMILY, 2).
-define(NFTA_NAT_REG_ADDR_MIN, 3).
-define(NFTA_NAT_REG_ADDR_MAX, 4).
-define(NFTA_NAT_REG_PROTO_MIN, 5).
-define(NFTA_NAT_REG_PROTO_MAX, 6).
-define(NFTA_NAT_FLAGS, 7).

-doc "Encode a nat expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"nat">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> binary().
name() -> <<"nat">>.

%% --- Attribute encoders ---

encode_attr(type, V) -> nfnl_attr:encode_u32(?NFTA_NAT_TYPE, V);
encode_attr(family, V) -> nfnl_attr:encode_u32(?NFTA_NAT_FAMILY, V);
encode_attr(reg_addr_min, V) -> nfnl_attr:encode_u32(?NFTA_NAT_REG_ADDR_MIN, V);
encode_attr(reg_addr_max, V) -> nfnl_attr:encode_u32(?NFTA_NAT_REG_ADDR_MAX, V);
encode_attr(reg_proto_min, V) -> nfnl_attr:encode_u32(?NFTA_NAT_REG_PROTO_MIN, V);
encode_attr(reg_proto_max, V) -> nfnl_attr:encode_u32(?NFTA_NAT_REG_PROTO_MAX, V);
encode_attr(flags, V) -> nfnl_attr:encode_u32(?NFTA_NAT_FLAGS, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_nat_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode nat attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_NAT_TYPE, <<V:32/big>>}, Acc) -> Acc#{type => V};
decode_attr({?NFTA_NAT_FAMILY, <<V:32/big>>}, Acc) -> Acc#{family => V};
decode_attr({?NFTA_NAT_REG_ADDR_MIN, <<V:32/big>>}, Acc) -> Acc#{reg_addr_min => V};
decode_attr({?NFTA_NAT_REG_ADDR_MAX, <<V:32/big>>}, Acc) -> Acc#{reg_addr_max => V};
decode_attr({?NFTA_NAT_REG_PROTO_MIN, <<V:32/big>>}, Acc) -> Acc#{reg_proto_min => V};
decode_attr({?NFTA_NAT_REG_PROTO_MAX, <<V:32/big>>}, Acc) -> Acc#{reg_proto_max => V};
decode_attr({?NFTA_NAT_FLAGS, <<V:32/big>>}, Acc) -> Acc#{flags => V};
decode_attr(_, Acc) -> Acc.
