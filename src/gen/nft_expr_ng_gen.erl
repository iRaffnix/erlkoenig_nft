-module(nft_expr_ng_gen).
-moduledoc """
nf_tables ng expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_NG_DREG = 1  (u32)  destination register
%%   NFTA_NG_MODULUS = 2  (u32)  maximum counter value
%%   NFTA_NG_TYPE = 3  (u32)  operation type
%%   NFTA_NG_OFFSET = 4  (u32)  offset to be added to the counter

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_ng.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_NG_DREG, 1).
-define(NFTA_NG_MODULUS, 2).
-define(NFTA_NG_TYPE, 3).
-define(NFTA_NG_OFFSET, 4).

-doc "Encode a ng expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"ng">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> <<_:16>>.
name() -> <<"ng">>.

%% --- Attribute encoders ---

encode_attr(dreg, V) -> nfnl_attr:encode_u32(?NFTA_NG_DREG, V);
encode_attr(modulus, V) -> nfnl_attr:encode_u32(?NFTA_NG_MODULUS, V);
encode_attr(type, V) -> nfnl_attr:encode_u32(?NFTA_NG_TYPE, V);
encode_attr(offset, V) -> nfnl_attr:encode_u32(?NFTA_NG_OFFSET, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_ng_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode ng attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_NG_DREG, <<V:32/big>>}, Acc) -> Acc#{dreg => V};
decode_attr({?NFTA_NG_MODULUS, <<V:32/big>>}, Acc) -> Acc#{modulus => V};
decode_attr({?NFTA_NG_TYPE, <<V:32/big>>}, Acc) -> Acc#{type => V};
decode_attr({?NFTA_NG_OFFSET, <<V:32/big>>}, Acc) -> Acc#{offset => V};
decode_attr(_, Acc) -> Acc.
