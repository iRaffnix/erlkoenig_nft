-module(nft_expr_objref_gen).
-moduledoc """
nf_tables objref expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_OBJREF_IMM_TYPE = 1  (u32)  object type for immediate reference
%%   NFTA_OBJREF_IMM_NAME = 2  (string)  object name for immediate reference
%%   NFTA_OBJREF_SET_SREG = 3  (u32)  source register of the data to look for
%%   NFTA_OBJREF_SET_NAME = 4  (string)  name of the set where to look for
%%   NFTA_OBJREF_SET_ID = 5  (u32)  id of the set where to look for in this transaction

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_objref.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_OBJREF_IMM_TYPE, 1).
-define(NFTA_OBJREF_IMM_NAME, 2).
-define(NFTA_OBJREF_SET_SREG, 3).
-define(NFTA_OBJREF_SET_NAME, 4).
-define(NFTA_OBJREF_SET_ID, 5).

-doc "Encode a objref expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"objref">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> <<_:48>>.
name() -> <<"objref">>.

%% --- Attribute encoders ---

encode_attr(imm_type, V) -> nfnl_attr:encode_u32(?NFTA_OBJREF_IMM_TYPE, V);
encode_attr(imm_name, V) -> nfnl_attr:encode_str(?NFTA_OBJREF_IMM_NAME, V);
encode_attr(set_sreg, V) -> nfnl_attr:encode_u32(?NFTA_OBJREF_SET_SREG, V);
encode_attr(set_name, V) -> nfnl_attr:encode_str(?NFTA_OBJREF_SET_NAME, V);
encode_attr(set_id, V) -> nfnl_attr:encode_u32(?NFTA_OBJREF_SET_ID, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_objref_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode objref attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_OBJREF_IMM_TYPE, <<V:32/big>>}, Acc) -> Acc#{imm_type => V};
decode_attr({?NFTA_OBJREF_IMM_NAME, V}, Acc) when is_binary(V) -> Acc#{imm_name => strip_null(V)};
decode_attr({?NFTA_OBJREF_SET_SREG, <<V:32/big>>}, Acc) -> Acc#{set_sreg => V};
decode_attr({?NFTA_OBJREF_SET_NAME, V}, Acc) when is_binary(V) -> Acc#{set_name => strip_null(V)};
decode_attr({?NFTA_OBJREF_SET_ID, <<V:32/big>>}, Acc) -> Acc#{set_id => V};
decode_attr(_, Acc) -> Acc.

%% --- Internal ---

strip_null(Bin) when is_binary(Bin) ->
    case binary:match(Bin, <<0>>) of
        {Pos, _} -> binary:part(Bin, 0, Pos);
        nomatch -> Bin
    end.
