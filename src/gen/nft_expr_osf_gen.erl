-module(nft_expr_osf_gen).
-moduledoc """
nf_tables osf expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_OSF_DREG = 1  (u32)  destination register
%%   NFTA_OSF_TTL = 2  (u8)  Value of the TTL osf option
%%   NFTA_OSF_FLAGS = 3  (u32)  flags

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_osf.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_OSF_DREG, 1).
-define(NFTA_OSF_TTL, 2).
-define(NFTA_OSF_FLAGS, 3).

-doc "Encode a osf expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"osf">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> binary().
name() -> <<"osf">>.

%% --- Attribute encoders ---

encode_attr(dreg, V) -> nfnl_attr:encode_u32(?NFTA_OSF_DREG, V);
encode_attr(ttl, V) -> nfnl_attr:encode(?NFTA_OSF_TTL, <<V:8>>);
encode_attr(flags, V) -> nfnl_attr:encode_u32(?NFTA_OSF_FLAGS, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_osf_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode osf attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_OSF_DREG, <<V:32/big>>}, Acc) -> Acc#{dreg => V};
decode_attr({?NFTA_OSF_TTL, <<V:8>>}, Acc) -> Acc#{ttl => V};
decode_attr({?NFTA_OSF_FLAGS, <<V:32/big>>}, Acc) -> Acc#{flags => V};
decode_attr(_, Acc) -> Acc.
