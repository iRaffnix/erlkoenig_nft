-module(nft_expr_payload_gen).
-moduledoc """
nf_tables payload expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_PAYLOAD_DREG = 1  (u32)  destination register to load data into
%%   NFTA_PAYLOAD_BASE = 2  (u32)  payload base
%%   NFTA_PAYLOAD_OFFSET = 3  (u32)  payload offset relative to base
%%   NFTA_PAYLOAD_LEN = 4  (u32)  payload length
%%   NFTA_PAYLOAD_SREG = 5  (u32)  source register to load data from
%%   NFTA_PAYLOAD_CSUM_TYPE = 6  (u32)  checksum type
%%   NFTA_PAYLOAD_CSUM_OFFSET = 7  (u32)  checksum offset relative to base
