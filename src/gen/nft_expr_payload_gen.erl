Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_payload.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_PAYLOAD_DREG, 1).
-define(NFTA_PAYLOAD_BASE, 2).
-define(NFTA_PAYLOAD_OFFSET, 3).
-define(NFTA_PAYLOAD_LEN, 4).
-define(NFTA_PAYLOAD_SREG, 5).
-define(NFTA_PAYLOAD_CSUM_TYPE, 6).
-define(NFTA_PAYLOAD_CSUM_OFFSET, 7).
-define(NFTA_PAYLOAD_CSUM_FLAGS, 8).

-doc "Encode a payload expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"payload">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> binary().
name() -> <<"payload">>.

%% --- Attribute encoders ---

encode_attr(dreg, V) -> nfnl_attr:encode_u32(?NFTA_PAYLOAD_DREG, V);
encode_attr(base, V) -> nfnl_attr:encode_u32(?NFTA_PAYLOAD_BASE, V);
encode_attr(offset, V) -> nfnl_attr:encode_u32(?NFTA_PAYLOAD_OFFSET, V);
encode_attr(len, V) -> nfnl_attr:encode_u32(?NFTA_PAYLOAD_LEN, V);
encode_attr(sreg, V) -> nfnl_attr:encode_u32(?NFTA_PAYLOAD_SREG, V);
encode_attr(csum_type, V) -> nfnl_attr:encode_u32(?NFTA_PAYLOAD_CSUM_TYPE, V);
encode_attr(csum_offset, V) -> nfnl_attr:encode_u32(?NFTA_PAYLOAD_CSUM_OFFSET, V);
encode_attr(csum_flags, V) -> nfnl_attr:encode_u32(?NFTA_PAYLOAD_CSUM_FLAGS, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_payload_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode payload attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_PAYLOAD_DREG, <<V:32/big>>}, Acc) -> Acc#{dreg => V};
decode_attr({?NFTA_PAYLOAD_BASE, <<V:32/big>>}, Acc) -> Acc#{base => V};
decode_attr({?NFTA_PAYLOAD_OFFSET, <<V:32/big>>}, Acc) -> Acc#{offset => V};
decode_attr({?NFTA_PAYLOAD_LEN, <<V:32/big>>}, Acc) -> Acc#{len => V};
decode_attr({?NFTA_PAYLOAD_SREG, <<V:32/big>>}, Acc) -> Acc#{sreg => V};
decode_attr({?NFTA_PAYLOAD_CSUM_TYPE, <<V:32/big>>}, Acc) -> Acc#{csum_type => V};
decode_attr({?NFTA_PAYLOAD_CSUM_OFFSET, <<V:32/big>>}, Acc) -> Acc#{csum_offset => V};
decode_attr({?NFTA_PAYLOAD_CSUM_FLAGS, <<V:32/big>>}, Acc) -> Acc#{csum_flags => V};
decode_attr(_, Acc) -> Acc.
