-module(nft_expr_queue_gen).
-moduledoc """
nf_tables queue expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_QUEUE_NUM = 1  (u16)  netlink queue to send messages to
%%   NFTA_QUEUE_TOTAL = 2  (u16)  number of queues to load balance packets on
%%   NFTA_QUEUE_FLAGS = 3  (u16)  various flags
%%   NFTA_QUEUE_SREG_QNUM = 4  (u32)  source register of queue number

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_queue.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_QUEUE_NUM, 1).
-define(NFTA_QUEUE_TOTAL, 2).
-define(NFTA_QUEUE_FLAGS, 3).
-define(NFTA_QUEUE_SREG_QNUM, 4).

-doc "Encode a queue expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"queue">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> binary().
name() -> <<"queue">>.

%% --- Attribute encoders ---

encode_attr(num, V) -> nfnl_attr:encode(?NFTA_QUEUE_NUM, <<V:16/big>>);
encode_attr(total, V) -> nfnl_attr:encode(?NFTA_QUEUE_TOTAL, <<V:16/big>>);
encode_attr(flags, V) -> nfnl_attr:encode(?NFTA_QUEUE_FLAGS, <<V:16/big>>);
encode_attr(sreg_qnum, V) -> nfnl_attr:encode_u32(?NFTA_QUEUE_SREG_QNUM, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_queue_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode queue attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_QUEUE_NUM, <<V:16/big>>}, Acc) -> Acc#{num => V};
decode_attr({?NFTA_QUEUE_TOTAL, <<V:16/big>>}, Acc) -> Acc#{total => V};
decode_attr({?NFTA_QUEUE_FLAGS, <<V:16/big>>}, Acc) -> Acc#{flags => V};
decode_attr({?NFTA_QUEUE_SREG_QNUM, <<V:32/big>>}, Acc) -> Acc#{sreg_qnum => V};
decode_attr(_, Acc) -> Acc.
