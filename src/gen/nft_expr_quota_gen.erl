-module(nft_expr_quota_gen).
-moduledoc """
nf_tables quota expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_QUOTA_BYTES = 1  (u64)  quota in bytes
%%   NFTA_QUOTA_FLAGS = 2  (u32)  flags
%%   NFTA_QUOTA_CONSUMED = 4  (u64)  quota already consumed in bytes

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_quota.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_QUOTA_BYTES, 1).
-define(NFTA_QUOTA_FLAGS, 2).
-define(NFTA_QUOTA_CONSUMED, 4).

-doc "Encode a quota expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"quota">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> binary().
name() -> <<"quota">>.

%% --- Attribute encoders ---

encode_attr(bytes, V) -> nfnl_attr:encode_u64(?NFTA_QUOTA_BYTES, V);
encode_attr(flags, V) -> nfnl_attr:encode_u32(?NFTA_QUOTA_FLAGS, V);
encode_attr(consumed, V) -> nfnl_attr:encode_u64(?NFTA_QUOTA_CONSUMED, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_quota_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode quota attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_QUOTA_BYTES, <<V:64/big>>}, Acc) -> Acc#{bytes => V};
decode_attr({?NFTA_QUOTA_FLAGS, <<V:32/big>>}, Acc) -> Acc#{flags => V};
decode_attr({?NFTA_QUOTA_CONSUMED, <<V:64/big>>}, Acc) -> Acc#{consumed => V};
decode_attr(_, Acc) -> Acc.
