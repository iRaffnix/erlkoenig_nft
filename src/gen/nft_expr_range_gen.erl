-module(nft_expr_range_gen).
-moduledoc """
nf_tables range expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_RANGE_SREG = 1  (u32)  source register of data to compare
%%   NFTA_RANGE_OP = 2  (u32)  cmp operation
%%   NFTA_RANGE_FROM_DATA = 3  (nested)  data range from
%%   NFTA_RANGE_TO_DATA = 4  (nested)  data range to

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_range.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_RANGE_SREG, 1).
-define(NFTA_RANGE_OP, 2).
-define(NFTA_RANGE_FROM_DATA, 3).
-define(NFTA_RANGE_TO_DATA, 4).

-doc "Encode a range expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"range">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> <<_:40>>.
name() -> <<"range">>.

%% --- Attribute encoders ---

encode_attr(sreg, V) -> nfnl_attr:encode_u32(?NFTA_RANGE_SREG, V);
encode_attr(op, V) -> nfnl_attr:encode_u32(?NFTA_RANGE_OP, V);
encode_attr(from_data, V) -> nfnl_attr:encode_nested(?NFTA_RANGE_FROM_DATA, V);
encode_attr(to_data, V) -> nfnl_attr:encode_nested(?NFTA_RANGE_TO_DATA, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_range_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode range attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_RANGE_SREG, <<V:32/big>>}, Acc) -> Acc#{sreg => V};
decode_attr({?NFTA_RANGE_OP, <<V:32/big>>}, Acc) -> Acc#{op => V};
decode_attr({?NFTA_RANGE_FROM_DATA, nested, V}, Acc) -> Acc#{from_data => V};
decode_attr({?NFTA_RANGE_TO_DATA, nested, V}, Acc) -> Acc#{to_data => V};
decode_attr(_, Acc) -> Acc.
