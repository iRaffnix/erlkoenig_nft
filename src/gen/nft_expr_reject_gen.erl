-module(nft_expr_reject_gen).
-moduledoc """
nf_tables reject expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_REJECT_TYPE = 1  (u32)  packet type to use
%%   NFTA_REJECT_ICMP_CODE = 2  (u8)  ICMP code to use

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_reject.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_REJECT_TYPE, 1).
-define(NFTA_REJECT_ICMP_CODE, 2).

-doc "Encode a reject expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"reject">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> binary().
name() -> <<"reject">>.

%% --- Attribute encoders ---

encode_attr(type, V) -> nfnl_attr:encode_u32(?NFTA_REJECT_TYPE, V);
encode_attr(icmp_code, V) -> nfnl_attr:encode(?NFTA_REJECT_ICMP_CODE, <<V:8>>);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_reject_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode reject attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_REJECT_TYPE, <<V:32/big>>}, Acc) -> Acc#{type => V};
decode_attr({?NFTA_REJECT_ICMP_CODE, <<V:8>>}, Acc) -> Acc#{icmp_code => V};
decode_attr(_, Acc) -> Acc.
