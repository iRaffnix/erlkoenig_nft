-module(nft_expr_rt_gen).
-moduledoc """
nf_tables rt expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_RT_DREG = 1  (u32)  destination register
%%   NFTA_RT_KEY = 2  (u32)  routing data item to load

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_rt.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_RT_DREG, 1).
-define(NFTA_RT_KEY, 2).

-doc "Encode a rt expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"rt">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> <<_:16>>.
name() -> <<"rt">>.

%% --- Attribute encoders ---

encode_attr(dreg, V) -> nfnl_attr:encode_u32(?NFTA_RT_DREG, V);
encode_attr(key, V) -> nfnl_attr:encode_u32(?NFTA_RT_KEY, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_rt_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode rt attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_RT_DREG, <<V:32/big>>}, Acc) -> Acc#{dreg => V};
decode_attr({?NFTA_RT_KEY, <<V:32/big>>}, Acc) -> Acc#{key => V};
decode_attr(_, Acc) -> Acc.
