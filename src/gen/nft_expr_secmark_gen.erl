-module(nft_expr_secmark_gen).
-moduledoc """
nf_tables secmark expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_SECMARK_CTX = 1  (string)  security context

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_secmark.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_SECMARK_CTX, 1).

-doc "Encode a secmark expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"secmark">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> <<_:56>>.
name() -> <<"secmark">>.

%% --- Attribute encoders ---

encode_attr(ctx, V) -> nfnl_attr:encode_str(?NFTA_SECMARK_CTX, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_secmark_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode secmark attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_SECMARK_CTX, V}, Acc) when is_binary(V) -> Acc#{ctx => strip_null(V)};
decode_attr(_, Acc) -> Acc.

%% --- Internal ---

strip_null(Bin) when is_binary(Bin) ->
    case binary:match(Bin, <<0>>) of
        {Pos, _} -> binary:part(Bin, 0, Pos);
        nomatch -> Bin
    end.
