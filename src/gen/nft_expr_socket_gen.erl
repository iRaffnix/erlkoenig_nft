-module(nft_expr_socket_gen).
-moduledoc """
nf_tables socket expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_SOCKET_KEY = 1  (unknown)  socket key to match
%%   NFTA_SOCKET_DREG = 2  (unknown)  destination register
%%   NFTA_SOCKET_LEVEL = 3  (unknown)  cgroups2 ancestor level (only for cgroupsv2)

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_socket.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_SOCKET_KEY, 1).
-define(NFTA_SOCKET_DREG, 2).
-define(NFTA_SOCKET_LEVEL, 3).

-doc "Encode a socket expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"socket">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> binary().
name() -> <<"socket">>.

%% --- Attribute encoders ---

encode_attr(key, V) -> nfnl_attr:encode_u32(?NFTA_SOCKET_KEY, V);
encode_attr(dreg, V) -> nfnl_attr:encode_u32(?NFTA_SOCKET_DREG, V);
encode_attr(level, V) -> nfnl_attr:encode_u32(?NFTA_SOCKET_LEVEL, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_socket_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode socket attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_SOCKET_KEY, <<V:32/big>>}, Acc) -> Acc#{key => V};
decode_attr({?NFTA_SOCKET_DREG, <<V:32/big>>}, Acc) -> Acc#{dreg => V};
decode_attr({?NFTA_SOCKET_LEVEL, <<V:32/big>>}, Acc) -> Acc#{level => V};
decode_attr(_, Acc) -> Acc.
