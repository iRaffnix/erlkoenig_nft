-module(nft_expr_synproxy_gen).
-moduledoc """
nf_tables synproxy expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_SYNPROXY_MSS = 1  (u16)  mss value sent to the backend
%%   NFTA_SYNPROXY_WSCALE = 2  (u8)  wscale value sent to the backend
%%   NFTA_SYNPROXY_FLAGS = 3  (u32)  flags

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_synproxy.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_SYNPROXY_MSS, 1).
-define(NFTA_SYNPROXY_WSCALE, 2).
-define(NFTA_SYNPROXY_FLAGS, 3).

-doc "Encode a synproxy expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"synproxy">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> <<_:64>>.
name() -> <<"synproxy">>.

%% --- Attribute encoders ---

encode_attr(mss, V) -> nfnl_attr:encode(?NFTA_SYNPROXY_MSS, <<V:16/big>>);
encode_attr(wscale, V) -> nfnl_attr:encode(?NFTA_SYNPROXY_WSCALE, <<V:8>>);
encode_attr(flags, V) -> nfnl_attr:encode_u32(?NFTA_SYNPROXY_FLAGS, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_synproxy_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode synproxy attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_SYNPROXY_MSS, <<V:16/big>>}, Acc) -> Acc#{mss => V};
decode_attr({?NFTA_SYNPROXY_WSCALE, <<V:8>>}, Acc) -> Acc#{wscale => V};
decode_attr({?NFTA_SYNPROXY_FLAGS, <<V:32/big>>}, Acc) -> Acc#{flags => V};
decode_attr(_, Acc) -> Acc.
