-export([encode/1, decode/1, name/0]).

-define(NFTA_TPROXY_FAMILY, 1).
-define(NFTA_TPROXY_REG_ADDR, 2).
-define(NFTA_TPROXY_REG_PORT, 3).

-doc "Encode a tproxy expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"tproxy">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> <<_:48>>.
name() -> <<"tproxy">>.

%% --- Attribute encoders ---

encode_attr(family, V) -> nfnl_attr:encode_u32(?NFTA_TPROXY_FAMILY, V);
encode_attr(reg_addr, V) -> nfnl_attr:encode_u32(?NFTA_TPROXY_REG_ADDR, V);
encode_attr(reg_port, V) -> nfnl_attr:encode_u32(?NFTA_TPROXY_REG_PORT, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_tproxy_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode tproxy attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_TPROXY_FAMILY, V}, Acc) -> Acc#{family => V};
decode_attr({?NFTA_TPROXY_REG_ADDR, V}, Acc) -> Acc#{reg_addr => V};
decode_attr({?NFTA_TPROXY_REG_PORT, V}, Acc) -> Acc#{reg_port => V};
decode_attr(_, Acc) -> Acc.
