-module(nft_expr_tunnel_gen).
-moduledoc """
nf_tables tunnel expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_TUNNEL_KEY = 1  (unknown)  
%%   NFTA_TUNNEL_DREG = 2  (unknown)  
%%   NFTA_TUNNEL_MODE = 3  (unknown)  

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_tunnel.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_TUNNEL_KEY, 1).
-define(NFTA_TUNNEL_DREG, 2).
-define(NFTA_TUNNEL_MODE, 3).

-doc "Encode a tunnel expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"tunnel">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> binary().
name() -> <<"tunnel">>.

%% --- Attribute encoders ---

encode_attr(key, V) -> nfnl_attr:encode_u32(?NFTA_TUNNEL_KEY, V);
encode_attr(dreg, V) -> nfnl_attr:encode_u32(?NFTA_TUNNEL_DREG, V);
encode_attr(mode, V) -> nfnl_attr:encode_u32(?NFTA_TUNNEL_MODE, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_tunnel_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode tunnel attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_TUNNEL_KEY, V}, Acc) -> Acc#{key => V};
decode_attr({?NFTA_TUNNEL_DREG, V}, Acc) -> Acc#{dreg => V};
decode_attr({?NFTA_TUNNEL_MODE, V}, Acc) -> Acc#{mode => V};
decode_attr(_, Acc) -> Acc.
