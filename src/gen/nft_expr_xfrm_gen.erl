-module(nft_expr_xfrm_gen).
-moduledoc """
nf_tables xfrm expression encoder/decoder (auto-generated).

Attributes:
%%   NFTA_XFRM_DREG = 1  (unknown)  
%%   NFTA_XFRM_KEY = 2  (unknown)  
%%   NFTA_XFRM_DIR = 3  (unknown)  
%%   NFTA_XFRM_SPNUM = 4  (unknown)  

Generated by nft_gen.escript from nf_tables.h.
Do not edit - extend via nft_expr_xfrm.erl instead.
""".

-export([encode/1, decode/1, name/0]).

-define(NFTA_XFRM_DREG, 1).
-define(NFTA_XFRM_KEY, 2).
-define(NFTA_XFRM_DIR, 3).
-define(NFTA_XFRM_SPNUM, 4).

-doc "Encode a xfrm expression from a map of attributes.".
-spec encode(map()) -> binary().
encode(Opts) when is_map(Opts) ->
    Attrs = iolist_to_binary(
        maps:fold(fun(K, V, Acc) -> [encode_attr(K, V) | Acc] end, [], Opts)),
    nft_expr:build(<<"xfrm">>, Attrs).

-doc "Return the Netlink expression name.".
-spec name() -> <<_:32>>.
name() -> <<"xfrm">>.

%% --- Attribute encoders ---

encode_attr(dreg, V) -> nfnl_attr:encode_u32(?NFTA_XFRM_DREG, V);
encode_attr(key, V) -> nfnl_attr:encode_u32(?NFTA_XFRM_KEY, V);
encode_attr(dir, V) -> nfnl_attr:encode_u32(?NFTA_XFRM_DIR, V);
encode_attr(spnum, V) -> nfnl_attr:encode_u32(?NFTA_XFRM_SPNUM, V);
encode_attr(Key, Val) ->
    logger:warning("nft_expr_xfrm_gen: unknown attr ~p=~p dropped", [Key, Val]),
    <<>>.

-doc "Decode xfrm attributes from a Netlink attribute list.".
-spec decode([tuple()]) -> map().
decode(AttrList) when is_list(AttrList) ->
    lists:foldl(fun decode_attr/2, #{}, AttrList).

%% --- Attribute decoders ---

decode_attr({?NFTA_XFRM_DREG, V}, Acc) -> Acc#{dreg => V};
decode_attr({?NFTA_XFRM_KEY, V}, Acc) -> Acc#{key => V};
decode_attr({?NFTA_XFRM_DIR, V}, Acc) -> Acc#{dir => V};
decode_attr({?NFTA_XFRM_SPNUM, V}, Acc) -> Acc#{spnum => V};
decode_attr(_, Acc) -> Acc.
